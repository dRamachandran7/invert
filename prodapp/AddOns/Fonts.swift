import Foundation
import SwiftUI

extension Font {
    static func satoshiMedium(_ size: CGFloat) -> Font { .custom("false", size: size) }
    static func panchangSemiBold (_ size: CGFloat) -> Font { .custom("Panchang-Semibold", size: size) }
    static func nexaHeavy(_ size: CGFloat) -> Font { .custom("Nexa-Heavy", size: size) }
    
}
